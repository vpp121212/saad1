import type { Incident, ImmediateAction } from '../types'

let idCounter = 1000

function makeIncident(overrides: Partial<Incident> = {}): Incident {
  const d = new Date()
  const days = ['Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday']
  const inc: Incident = {
    id: `INC-${++idCounter}`,
    station: 'Qasr Al Hokm',
    date: d.toISOString().slice(0, 10),
    day: days[d.getDay()],
    shift: 'Morning',
    smName: '',
    somName: '',
    reportedBy: '',
    sourceCCTV: false,
    sourceIntercom: false,
    sourcePassenger: false,
    sourceStaff: false,
    discoveryTime: '09:00',
    occNotificationTime: '',
    occResponseTime: '',
    somArrivalTime: '',
    smArrivalTime: '',
    category: 'Passenger Medical',
    severity: 'Medium',
    description: 'Incident description',
    location: 'Platform 01',
    locationDetail: '',
    emergencyCode: '',
    permitNumber: '',
    actions: [],
    supportRequested: '',
    occInstructions: '',
    finalResolution: '',
    cctvReference: '',
    intercomLog: '',
    images: [],
    somSignature: '',
    somSignTime: '',
    smSignature: '',
    smSignTime: '',
    submitterName: '',
    submitterTitle: '',
    submitterDept: '',
    submitterDate: '',
    submitterSignature: '',
    timeline: [],
    status: 'Open',
    createdAt: new Date().toISOString(),
    closedAt: '',
    ...overrides,
  }
  return inc
}

const act = (t: string, a: string, b: string, n = ''): ImmediateAction => ({ id: `a${Date.now()}-${Math.random().toString(36).slice(2, 6)}`, time: t, action: a, by: b, notes: n })

